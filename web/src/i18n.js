const state = reactive({
  locale: localStorage.getItem('locale') || LOCALES.zhCN,
})

function getMessage(locale, key) {
  const parts = key.split('.')
  let current = messages[locale]
  for (const part of parts) {
    current = current?.[part]
    if (current == null) return null
  }
  return current
}

export function t(key, params = {}) {
  const message = getMessage(state.locale, key) ?? getMessage(LOCALES.zhCN, key) ?? key
  if (typeof message !== 'string') return key
  return message.replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? `{${name}}`))
}

export function setLocale(locale) {
  state.locale = locale
  localStorage.setItem('locale', locale)
  document.documentElement.lang = locale
}

export function getLocale() {
  return state.locale
}

export function createI18nPlugin() {
  document.documentElement.lang = state.locale
  return {
    install(app) {
      const api = {
        state,
        t,
        setLocale,
        getLocale,
        locales: LOCALES,
      }
      app.provide(I18N_SYMBOL, api)
      app.config.globalProperties.$t = t
    },
  }
}

export function useI18n() {
  const i18n = inject(I18N_SYMBOL)
  if (!i18n) {
    throw new Error('i18n plugin is not installed')
  }
  return i18n
}
